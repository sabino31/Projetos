#include <stdio.h>

// Programa para demonstrar o operador de incremento
int main() {
    int numero1 = 1;

    // Primeiro incremento
    printf("Antes do incremento: %d\n", numero1);
    numero1++;
    printf("Após o incremento: %d\n\n", numero1);

    // Segundo incremento
    printf("Antes do incremento: %d\n", numero1);
    numero1++;
    printf("Após o incremento: %d\n", numero1);

    return 0;
}
